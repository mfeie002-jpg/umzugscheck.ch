// Helper function to get companies by region
export const getEnhancedCompaniesByRegion = (region?: string): CompanyData[] => {
  if (!region || region.toLowerCase() === "schweizweit") {
    return ENHANCED_COMPANIES;
  }
  
  return ENHANCED_COMPANIES.filter(company => 
    company.service_areas.some(area => 
      area.toLowerCase().includes(region.toLowerCase()) ||
      area.toLowerCase() === "schweizweit"
    )
  );
};

// Helper function to get companies by price level
export const getEnhancedCompaniesByPriceLevel = (priceLevel: string): CompanyData[] => {
  return ENHANCED_COMPANIES.filter(company => 
    company.price_level === priceLevel
  );
};
