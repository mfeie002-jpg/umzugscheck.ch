// Helper function to get companies by region
export const getCompaniesByRegion = (region?: string): CompanyData[] => {
  if (!region || region === "schweizweit") {
    return DEMO_COMPANIES;
  }
  
  return DEMO_COMPANIES.filter(company => 
    company.service_areas.some(area => 
      area.toLowerCase().includes(region.toLowerCase()) ||
      area.toLowerCase() === "schweizweit"
    )
  );
};

// Helper function to get companies by price level
export const getCompaniesByPriceLevel = (priceLevel: string): CompanyData[] => {
  return DEMO_COMPANIES.filter(company => 
    company.price_level === priceLevel || company.price_level === "fair"
  );
};
