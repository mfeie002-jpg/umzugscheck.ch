// Search function for autocomplete
export const searchPostalCodes = (query: string): SwissLocation[] => {
  if (!query || query.length < 2) return [];
  
  const normalizedQuery = query.toLowerCase().trim();
  
  return SWISS_POSTAL_CODES
    .filter(loc => 
      loc.plz.startsWith(normalizedQuery) || 
      loc.city.toLowerCase().includes(normalizedQuery)
    )
    .slice(0, 8); // Limit results
};

// Get city by PLZ
export const getCityByPLZ = (plz: string): SwissLocation | undefined => {
  return SWISS_POSTAL_CODES.find(loc => loc.plz === plz);
};

// Validate Swiss PLZ
export const isValidSwissPLZ = (plz: string): boolean => {
  return /^[1-9]\d{3}$/.test(plz) && SWISS_POSTAL_CODES.some(loc => loc.plz === plz);
};
